! ===== begin garch.f90 =====
! GARCH(1,1) module: simulation, parameter transforms, negative log-likelihood
! with analytical gradients with respect to unconstrained parameters.
!
! Parameterisation (constrained):
!   h_t = omega + alpha*y_{t-1}^2 + beta*h_{t-1},  h_1 = omega/(1-alpha-beta)
!   omega > 0,  alpha > 0,  beta > 0,  alpha+beta < 1
!
! Unconstrained map  p -> (omega, alpha, beta):
!   omega = exp(p1)
!   alpha = exp(p2) / (1 + exp(p2) + exp(p3))
!   beta  = exp(p3) / (1 + exp(p2) + exp(p3))
! Inverse:
!   p1 = log(omega)
!   p2 = log(alpha / (1-alpha-beta))
!   p3 = log(beta  / (1-alpha-beta))

module garch_mod
    use kind_mod,       only: dp
    use math_const_mod, only: log_sqrt_2pi, two_pi
    implicit none
    private

    ! data shared with objective callback
    real(dp), allocatable, save :: g_obs(:)
    integer,               save :: g_nobs = 0
    real(dp),              save :: g_target_var = 0.0_dp   ! variance target for VT variant

    public :: garch_set_data, garch_simulate, garch_obj, &
              garch_transform, garch_inv_transform, &
              garch_vt_set_target, garch_vt_transform, garch_vt_inv_transform, garch_vt_obj

contains

    ! Store observations for use by garch_obj.
    subroutine garch_set_data(y, n)
        integer,  intent(in) :: n
        real(dp), intent(in) :: y(n)
        if (allocated(g_obs)) deallocate(g_obs)
        allocate(g_obs(n))
        g_obs  = y
        g_nobs = n
    end subroutine garch_set_data

    ! Unconstrained p(3) -> constrained (omega, alpha, beta).
    subroutine garch_transform(p, omega, alpha, beta)
        real(dp), intent(in)  :: p(3)
        real(dp), intent(out) :: omega, alpha, beta
        real(dp) :: e2, e3, s
        omega = exp(p(1))
        e2    = exp(p(2))
        e3    = exp(p(3))
        s     = 1.0_dp + e2 + e3
        alpha = e2 / s
        beta  = e3 / s
    end subroutine garch_transform

    ! Constrained (omega, alpha, beta) -> unconstrained p(3).
    ! Requires omega > 0, alpha > 0, beta > 0, alpha+beta < 1.
    subroutine garch_inv_transform(omega, alpha, beta, p)
        real(dp), intent(in)  :: omega, alpha, beta
        real(dp), intent(out) :: p(3)
        real(dp) :: gamma
        gamma = 1.0_dp - alpha - beta
        p(1)  = log(omega)
        p(2)  = log(alpha / gamma)
        p(3)  = log(beta  / gamma)
    end subroutine garch_inv_transform

    ! Simulate n observations from GARCH(1,1) using Box-Muller normals.
    ! h_1 is set to the unconditional variance omega/(1-alpha-beta).
    subroutine garch_simulate(omega, alpha, beta, n, seed_val, y)
        real(dp), intent(in)  :: omega, alpha, beta
        integer,  intent(in)  :: n, seed_val
        real(dp), intent(out) :: y(n)
        real(dp) :: h, u1, u2, eps
        integer  :: i, sz
        integer, allocatable :: seed_arr(:)

        call random_seed(size=sz)
        allocate(seed_arr(sz))
        seed_arr = seed_val
        call random_seed(put=seed_arr)
        deallocate(seed_arr)

        h = omega / (1.0_dp - alpha - beta)
        do i = 1, n
            ! guard against log(0) in Box-Muller
            do
                call random_number(u1)
                if (u1 > 0.0_dp) exit
            end do
            call random_number(u2)
            eps  = sqrt(-2.0_dp * log(u1)) * cos(two_pi * u2)
            y(i) = sqrt(h) * eps
            h    = omega + alpha * y(i)**2 + beta * h
        end do
    end subroutine garch_simulate

    ! Negative log-likelihood and its gradient w.r.t. unconstrained p.
    !
    ! f = -loglik = 0.5 * sum_t [ log(h_t) + y_t^2/h_t ]   (constant omitted)
    !
    ! Recurrences for dh_t/d_theta (theta = omega, alpha, beta):
    !   dh_t/d_omega = 1            + beta * dh_{t-1}/d_omega
    !   dh_t/d_alpha = y_{t-1}^2   + beta * dh_{t-1}/d_alpha
    !   dh_t/d_beta  = h_{t-1}     + beta * dh_{t-1}/d_beta
    !   initialised from h_1 = omega/(1-alpha-beta)
    !
    ! Chain rule from constrained to unconstrained (softmax Jacobian):
    !   d_omega/d_p1 = omega
    !   d_alpha/d_p2 = alpha*(1-alpha),  d_alpha/d_p3 = -alpha*beta
    !   d_beta /d_p2 = -alpha*beta,      d_beta /d_p3 =  beta*(1-beta)
    subroutine garch_obj(p, np, f, g)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f
        real(dp), intent(out) :: g(np)

        real(dp) :: omega, alpha, beta
        real(dp) :: h, dh_dom, dh_dal, dh_dbe
        real(dp) :: grad_om, grad_al, grad_be
        real(dp) :: h_unc, factor
        integer  :: t

        call garch_transform(p, omega, alpha, beta)

        h_unc  = omega / (1.0_dp - alpha - beta)
        h      = h_unc
        dh_dom = 1.0_dp / (1.0_dp - alpha - beta)
        dh_dal = h_unc  / (1.0_dp - alpha - beta)
        dh_dbe = h_unc  / (1.0_dp - alpha - beta)

        f       = real(g_nobs, dp) * log_sqrt_2pi
        grad_om = 0.0_dp
        grad_al = 0.0_dp
        grad_be = 0.0_dp

        do t = 1, g_nobs
            factor  =  1.0_dp/h - g_obs(t)**2 / h**2
            f       = f       + 0.5_dp * (log(h) + g_obs(t)**2 / h)
            grad_om = grad_om + 0.5_dp * factor * dh_dom
            grad_al = grad_al + 0.5_dp * factor * dh_dal
            grad_be = grad_be + 0.5_dp * factor * dh_dbe
            ! advance recurrences to h_{t+1}
            dh_dom = 1.0_dp          + beta * dh_dom
            dh_dal = g_obs(t)**2     + beta * dh_dal
            dh_dbe = h               + beta * dh_dbe
            h      = omega + alpha * g_obs(t)**2 + beta * h
        end do

        ! chain rule: constrained gradients -> unconstrained gradients
        g(1) =  grad_om * omega
        g(2) =  grad_al * alpha*(1.0_dp - alpha) - grad_be * alpha*beta
        g(3) = -grad_al * alpha*beta             + grad_be * beta*(1.0_dp - beta)

        ! normalise by n so that f and ||g|| are O(1) regardless of sample size
        f = f / g_nobs
        g = g / g_nobs
    end subroutine garch_obj

    ! ── Variance-targeted GARCH ───────────────────────────────────────────────
    ! omega is derived: omega = target*(1-alpha-beta), so h_1 = target exactly.
    ! Reduces stage-1 parameters from 3 to 2 per asset.

    subroutine garch_vt_set_target(target_var)
        ! Store per-asset unconditional variance target before calling garch_vt_obj.
        real(dp), intent(in) :: target_var  ! sample variance of demeaned returns
        g_target_var = target_var
    end subroutine garch_vt_set_target

    subroutine garch_vt_transform(p, alpha, beta)
        ! p(1:2) -> (alpha, beta) via 2-component softmax.
        real(dp), intent(in)  :: p(2)
        real(dp), intent(out) :: alpha, beta
        real(dp) :: e1, e2, s
        e1    = exp(p(1));  e2 = exp(p(2))
        s     = 1.0_dp + e1 + e2
        alpha = e1 / s;     beta = e2 / s
    end subroutine garch_vt_transform

    subroutine garch_vt_inv_transform(alpha, beta, p)
        ! (alpha, beta) -> p(1:2).
        real(dp), intent(in)  :: alpha, beta
        real(dp), intent(out) :: p(2)
        real(dp) :: gamma
        gamma = 1.0_dp - alpha - beta
        p(1)  = log(alpha / gamma)
        p(2)  = log(beta  / gamma)
    end subroutine garch_vt_inv_transform

    subroutine garch_vt_obj(p, np, f, g)
        ! NLL/n and gradient for variance-targeted GARCH(1,1). np=2.
        !
        ! omega = target*(1-alpha-beta),  h_1 = target  (dh_1/d* = 0).
        !
        ! Derivative recurrences (domega/dalpha = -target, domega/dbeta = -target):
        !   dh_t/dalpha = -target + y_{t-1}^2 + beta * dh_{t-1}/dalpha
        !   dh_t/dbeta  = -target + h_{t-1}   + beta * dh_{t-1}/dbeta
        !
        ! Chain rule (2-component softmax Jacobian):
        !   g(1) =  grad_al * alpha*(1-alpha) - grad_be * alpha*beta
        !   g(2) = -grad_al * alpha*beta      + grad_be * beta*(1-beta)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f, g(np)
        real(dp) :: alpha, beta, omega, h, dh_dal, dh_dbe, grad_al, grad_be, factor
        integer  :: t
        call garch_vt_transform(p, alpha, beta)
        omega  = g_target_var * (1.0_dp - alpha - beta)
        h      = g_target_var    ! h_1 = target by construction
        dh_dal = 0.0_dp          ! dh_1/dalpha = 0
        dh_dbe = 0.0_dp          ! dh_1/dbeta  = 0
        f       = real(g_nobs, dp) * log_sqrt_2pi
        grad_al = 0.0_dp;  grad_be = 0.0_dp
        do t = 1, g_nobs
            factor  = 1.0_dp/h - g_obs(t)**2 / h**2
            f       = f       + 0.5_dp * (log(h) + g_obs(t)**2 / h)
            grad_al = grad_al + 0.5_dp * factor * dh_dal
            grad_be = grad_be + 0.5_dp * factor * dh_dbe
            dh_dal  = -g_target_var + g_obs(t)**2 + beta * dh_dal
            dh_dbe  = -g_target_var + h            + beta * dh_dbe
            h       = omega + alpha * g_obs(t)**2  + beta * h
        end do
        g(1) =  grad_al * alpha*(1.0_dp - alpha) - grad_be * alpha*beta
        g(2) = -grad_al * alpha*beta             + grad_be * beta*(1.0_dp - beta)
        f = f / g_nobs;  g = g / g_nobs
    end subroutine garch_vt_obj

end module garch_mod

! ===== end garch.f90 =====

! ===== begin nagarch.f90 =====
! GARCH(1,1) with NAGARCH variance equation and Normal innovations.
!
! Variance equation:
!   h_t = omega + alpha*q_{t-1}^2 + beta*h_{t-1}
!   q_t = y_t - theta*sqrt(h_t) by default, or max(theta*sqrt(h_t)-y_t, 0)
!         when zero-above-shift news impact is enabled.
!   h_1 = omega / (1 - alpha*E(q_t^2/h_t) - beta)
!   omega > 0,  alpha > 0,  beta > 0,  theta unconstrained
!   Stationarity: alpha*(1+theta^2) + beta < 1
!
! Unconstrained parameterisation  p(4) -> (omega, alpha, beta, theta):
!   Let aa = alpha*(1+theta^2),  S = 1 + exp(p2) + exp(p3)
!   omega = exp(p1)
!   aa    = exp(p2) / S       ! satisfies aa > 0, aa+beta < 1
!   beta  = exp(p3) / S
!   alpha = aa / (1+theta^2)
!   theta = p4                ! unconstrained
! Inverse:
!   p1 = log(omega)
!   p2 = log(alpha*(1+theta^2) / (1 - alpha*(1+theta^2) - beta))
!   p3 = log(beta              / (1 - alpha*(1+theta^2) - beta))
!   p4 = theta

module nagarch_mod
    use kind_mod,       only: dp
    use math_const_mod, only: log_sqrt_2pi, two_pi, sqrt2
    implicit none
    private

    real(dp), allocatable, save :: gna_obs(:)  ! stored observations
    integer,               save :: gna_nobs = 0
    real(dp),              save :: gna_target_var = 0.0_dp   ! variance target for VT variant
    logical,               save :: gna_zero_above_shift = .false.

    public :: nagarch_set_data, nagarch_simulate, nagarch_obj, &
              nagarch_transform, nagarch_inv_transform, &
              nagarch_vt_set_target, nagarch_vt_transform, nagarch_vt_inv_transform, nagarch_vt_obj, &
              nagarch_set_news_impact, nagarch_news_impact_zero_above

contains

    subroutine nagarch_set_news_impact(zero_above_shift)
        logical, intent(in) :: zero_above_shift
        gna_zero_above_shift = zero_above_shift
    end subroutine nagarch_set_news_impact

    logical function nagarch_news_impact_zero_above()
        nagarch_news_impact_zero_above = gna_zero_above_shift
    end function nagarch_news_impact_zero_above

    subroutine nagarch_set_data(y, n)
        ! Store observations for use by nagarch_obj.
        integer,  intent(in) :: n    ! number of observations
        real(dp), intent(in) :: y(n) ! return series
        if (allocated(gna_obs)) deallocate(gna_obs)
        allocate(gna_obs(n))
        gna_obs  = y
        gna_nobs = n
    end subroutine nagarch_set_data

    subroutine nagarch_transform(p, omega, alpha, beta, theta)
        ! Unconstrained p(4) -> constrained (omega, alpha, beta, theta).
        real(dp), intent(in)  :: p(4)
        real(dp), intent(out) :: omega, alpha, beta, theta  ! NAGARCH parameters
        real(dp) :: e2, e3, s, aa, shift_moment, dmoment_unused
        omega = exp(p(1))
        theta = p(4)
        call nagarch_shift_moments(theta, shift_moment, dmoment_unused)
        e2    = exp(p(2))
        e3    = exp(p(3))
        s     = 1.0_dp + e2 + e3
        aa    = e2 / s
        beta  = e3 / s
        alpha = aa / shift_moment
    end subroutine nagarch_transform

    subroutine nagarch_inv_transform(omega, alpha, beta, theta, p)
        ! Constrained (omega, alpha, beta, theta) -> unconstrained p(4).
        ! Requires omega > 0, alpha > 0, beta > 0, alpha*(1+theta^2)+beta < 1.
        real(dp), intent(in)  :: omega, alpha, beta, theta  ! NAGARCH parameters
        real(dp), intent(out) :: p(4)
        real(dp) :: aa, gam, shift_moment, dmoment_unused
        call nagarch_shift_moments(theta, shift_moment, dmoment_unused)
        aa   = alpha * shift_moment
        gam  = 1.0_dp - aa - beta            ! stationarity slack
        p(1) = log(omega)
        p(2) = log(aa   / gam)
        p(3) = log(beta / gam)
        p(4) = theta
    end subroutine nagarch_inv_transform

    subroutine nagarch_simulate(omega, alpha, beta, theta, n, seed_val, y)
        ! Simulate n observations from NAGARCH(1,1) with Normal innovations.
        ! Uses Box-Muller; h_1 is set to the unconditional variance.
        real(dp), intent(in)  :: omega, alpha, beta, theta  ! NAGARCH parameters
        integer,  intent(in)  :: n                          ! number of observations
        integer,  intent(in)  :: seed_val                   ! RNG seed
        real(dp), intent(out) :: y(n)                       ! simulated return series
        real(dp) :: h, sqrth, r, u1, u2, eps, shift_moment, dmoment_unused
        integer  :: i, sz
        integer, allocatable :: seed_arr(:)

        call random_seed(size=sz)
        allocate(seed_arr(sz))
        seed_arr = seed_val
        call random_seed(put=seed_arr)
        deallocate(seed_arr)

        call nagarch_shift_moments(theta, shift_moment, dmoment_unused)
        h = omega / (1.0_dp - alpha*shift_moment - beta)
        do i = 1, n
            do
                call random_number(u1)
                if (u1 > 0.0_dp) exit
            end do
            call random_number(u2)
            eps   = sqrt(-2.0_dp * log(u1)) * cos(two_pi * u2)
            sqrth = sqrt(h)
            y(i)  = sqrth * eps
            r     = y(i) - theta * sqrth     ! shifted residual
            if (gna_zero_above_shift .and. r > 0.0_dp) r = 0.0_dp
            h     = omega + alpha*r**2 + beta*h
        end do
    end subroutine nagarch_simulate

    subroutine nagarch_obj(p, np, f, g)
        ! NLL and gradient w.r.t. unconstrained p for NAGARCH(1,1) with Normal noise.
        !
        ! f = (1/n) * [ n*log_sqrt_2pi + 0.5*sum_t(log(h_t) + y_t^2/h_t) ]
        !
        ! Define r_t = y_{t-1} - theta*sqrt(h_{t-1})
        !        kappa_t = beta - alpha*theta*r_t / sqrt(h_{t-1})
        !
        ! Variance derivative recurrences (forcing terms differ per parameter):
        !   dh_t/d_omega = 1                          + kappa_t * dh_{t-1}/d_omega
        !   dh_t/d_alpha = r_t^2                      + kappa_t * dh_{t-1}/d_alpha
        !   dh_t/d_beta  = h_{t-1}                    + kappa_t * dh_{t-1}/d_beta
        !   dh_t/d_theta = -2*alpha*r_t*sqrt(h_{t-1}) + kappa_t * dh_{t-1}/d_theta
        !
        ! Initial values from h_1 = omega/D, D = 1 - alpha*(1+theta^2) - beta:
        !   dh_1/d_omega = 1/D
        !   dh_1/d_alpha = h_1*(1+theta^2)/D
        !   dh_1/d_beta  = h_1/D
        !   dh_1/d_theta = 2*alpha*theta*h_1/D
        !
        ! Chain rule to unconstrained params (aa = alpha*(1+theta^2), s2 = 1+theta^2):
        !   g(1) =  grad_om * omega
        !   g(2) =  grad_al * alpha*(1-aa) - grad_be * aa*beta
        !   g(3) = -grad_al * alpha*beta   + grad_be * beta*(1-beta)
        !   g(4) =  grad_al * (-2*theta*alpha/s2) + grad_th
        integer,  intent(in)  :: np     ! number of parameters (must be 4)
        real(dp), intent(in)  :: p(np)  ! unconstrained parameters
        real(dp), intent(out) :: f      ! NLL/n
        real(dp), intent(out) :: g(np)  ! gradient of NLL/n

        real(dp) :: omega, alpha, beta, theta
        real(dp) :: h, sqrth, r, kappa, s2, D, h_unc, aa, dmom
        real(dp) :: dh_dom, dh_dal, dh_dbe, dh_dth
        real(dp) :: grad_om, grad_al, grad_be, grad_th, factor
        logical  :: active
        integer  :: t

        call nagarch_transform(p, omega, alpha, beta, theta)

        call nagarch_shift_moments(theta, s2, dmom)
        D     = 1.0_dp - alpha*s2 - beta
        h_unc = omega / D

        ! initial variance derivatives from h_1 = omega/D
        h      = h_unc
        dh_dom = 1.0_dp / D
        dh_dal = h_unc * s2 / D
        dh_dbe = h_unc / D
        dh_dth = alpha * dmom * h_unc / D

        f       = real(gna_nobs, dp) * log_sqrt_2pi
        grad_om = 0.0_dp
        grad_al = 0.0_dp
        grad_be = 0.0_dp
        grad_th = 0.0_dp

        do t = 1, gna_nobs
            factor  =  1.0_dp/h - gna_obs(t)**2 / h**2
            f       = f       + 0.5_dp * (log(h) + gna_obs(t)**2 / h)
            grad_om = grad_om + 0.5_dp * factor * dh_dom
            grad_al = grad_al + 0.5_dp * factor * dh_dal
            grad_be = grad_be + 0.5_dp * factor * dh_dbe
            grad_th = grad_th + 0.5_dp * factor * dh_dth
            ! update recurrences for h_{t+1} using r_{t+1} = y_t - theta*sqrt(h_t)
            sqrth  = sqrt(h)
            r      = gna_obs(t) - theta * sqrth
            active = .true.
            if (gna_zero_above_shift .and. r > 0.0_dp) then
                r = 0.0_dp
                active = .false.
            end if
            kappa  = beta
            if (active) kappa = beta - alpha * theta * r / sqrth
            dh_dom = 1.0_dp                + kappa * dh_dom
            dh_dal = r**2                  + kappa * dh_dal
            dh_dbe = h                     + kappa * dh_dbe
            dh_dth = kappa * dh_dth
            if (active) dh_dth = dh_dth - 2.0_dp*alpha*r*sqrth
            h      = omega + alpha*r**2 + beta*h
        end do

        ! chain rule: constrained gradients -> unconstrained gradients
        aa   = alpha * s2
        g(1) =  grad_om * omega
        g(2) =  grad_al * alpha*(1.0_dp - aa) - grad_be * aa*beta
        g(3) = -grad_al * alpha*beta           + grad_be * beta*(1.0_dp - beta)
        g(4) =  grad_al * (-alpha*dmom/s2) + grad_th

        f = f / gna_nobs
        g = g / gna_nobs
    end subroutine nagarch_obj

    ! ── Variance-targeted NAGARCH ─────────────────────────────────────────────
    ! omega = target*(1-alpha*(1+theta^2)-beta), h_1 = target (dh_1/d* = 0).
    ! Reduces stage-1 parameters from 4 to 3 per asset.
    !
    ! Free parameters: p(1) = logit(aa), p(2) = logit(beta), p(3) = theta
    ! where aa = alpha*(1+theta^2), same softmax as standard but without omega.
    !
    ! Derivative recurrences (kappa = beta - alpha*theta*r_t/sqrt(h_t)):
    !   dh_{t+1}/dalpha = -target*s2 + r_t^2              + kappa*dh_t/dalpha
    !   dh_{t+1}/dbeta  = -target    + h_t                + kappa*dh_t/dbeta
    !   dh_{t+1}/dtheta = -2*target*alpha*theta - 2*alpha*r_t*sqrt(h_t) + kappa*dh_t/dtheta
    ! where s2 = 1+theta^2.

    subroutine nagarch_vt_set_target(target_var)
        ! Store per-asset unconditional variance target before calling nagarch_vt_obj.
        real(dp), intent(in) :: target_var  ! sample variance of demeaned returns
        gna_target_var = target_var
    end subroutine nagarch_vt_set_target

    subroutine nagarch_vt_transform(p, alpha, beta, theta)
        ! p(1:3) -> (alpha, beta, theta); aa=alpha*(1+theta^2) via softmax on p(1:2).
        real(dp), intent(in)  :: p(3)
        real(dp), intent(out) :: alpha, beta, theta
        real(dp) :: e1, e2, s, aa, shift_moment, dmoment_unused
        theta = p(3)
        call nagarch_shift_moments(theta, shift_moment, dmoment_unused)
        e1    = exp(p(1));  e2 = exp(p(2))
        s     = 1.0_dp + e1 + e2
        aa    = e1 / s;     beta  = e2 / s
        alpha = aa / shift_moment
    end subroutine nagarch_vt_transform

    subroutine nagarch_vt_inv_transform(alpha, beta, theta, p)
        ! (alpha, beta, theta) -> p(1:3).
        real(dp), intent(in)  :: alpha, beta, theta
        real(dp), intent(out) :: p(3)
        real(dp) :: aa, gam, shift_moment, dmoment_unused
        call nagarch_shift_moments(theta, shift_moment, dmoment_unused)
        aa   = alpha * shift_moment
        gam  = 1.0_dp - aa - beta
        p(1) = log(aa   / gam)
        p(2) = log(beta / gam)
        p(3) = theta
    end subroutine nagarch_vt_inv_transform

    subroutine nagarch_vt_obj(p, np, f, g)
        ! NLL/n and gradient for variance-targeted NAGARCH(1,1). np=3.
        !
        ! Chain rule to unconstrained (aa = alpha*(1+theta^2)):
        !   g(1) =  grad_al * alpha*(1-aa)       - grad_be * aa*beta
        !   g(2) = -grad_al * alpha*beta          + grad_be * beta*(1-beta)
        !   g(3) =  grad_al * (-2*theta*alpha/s2) + grad_th
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f, g(np)
        real(dp) :: alpha, beta, theta, omega, s2, aa, dmom
        real(dp) :: h, sqrth, r, kappa
        real(dp) :: dh_dal, dh_dbe, dh_dth
        real(dp) :: grad_al, grad_be, grad_th, factor
        logical  :: active
        integer  :: t
        call nagarch_vt_transform(p, alpha, beta, theta)
        call nagarch_shift_moments(theta, s2, dmom)
        omega = gna_target_var * (1.0_dp - alpha*s2 - beta)
        h     = gna_target_var     ! h_1 = target by construction
        dh_dal = 0.0_dp;  dh_dbe = 0.0_dp;  dh_dth = 0.0_dp
        f       = real(gna_nobs, dp) * log_sqrt_2pi
        grad_al = 0.0_dp;  grad_be = 0.0_dp;  grad_th = 0.0_dp
        do t = 1, gna_nobs
            factor  = 1.0_dp/h - gna_obs(t)**2 / h**2
            f       = f       + 0.5_dp * (log(h) + gna_obs(t)**2 / h)
            grad_al = grad_al + 0.5_dp * factor * dh_dal
            grad_be = grad_be + 0.5_dp * factor * dh_dbe
            grad_th = grad_th + 0.5_dp * factor * dh_dth
            sqrth  = sqrt(h)
            r      = gna_obs(t) - theta * sqrth
            active = .true.
            if (gna_zero_above_shift .and. r > 0.0_dp) then
                r = 0.0_dp
                active = .false.
            end if
            kappa  = beta
            if (active) kappa = beta - alpha * theta * r / sqrth
            dh_dal = -gna_target_var*s2                     + r**2             + kappa * dh_dal
            dh_dbe = -gna_target_var                        + h                + kappa * dh_dbe
            dh_dth = -gna_target_var*alpha*dmom + kappa * dh_dth
            if (active) dh_dth = dh_dth - 2.0_dp*alpha*r*sqrth
            h      = omega + alpha*r**2 + beta*h
        end do
        aa   = alpha * s2
        g(1) =  grad_al * alpha*(1.0_dp - aa)        - grad_be * aa*beta
        g(2) = -grad_al * alpha*beta                 + grad_be * beta*(1.0_dp - beta)
        g(3) =  grad_al * (-alpha*dmom/s2)            + grad_th
        f = f / gna_nobs;  g = g / gna_nobs
    end subroutine nagarch_vt_obj

    subroutine nagarch_shift_moments(theta, moment, dmoment)
        real(dp), intent(in)  :: theta
        real(dp), intent(out) :: moment, dmoment
        real(dp) :: cdf, pdf
        if (gna_zero_above_shift) then
            cdf     = 0.5_dp * (1.0_dp + erf(theta / sqrt2))
            pdf     = exp(-0.5_dp * theta**2 - log_sqrt_2pi)
            moment  = (1.0_dp + theta**2) * cdf + theta * pdf
            dmoment = 2.0_dp * (theta * cdf + pdf)
        else
            moment  = 1.0_dp + theta**2
            dmoment = 2.0_dp * theta
        end if
    end subroutine nagarch_shift_moments

end module nagarch_mod

! ===== end nagarch.f90 =====

! ===== begin gjr.f90 =====
! GARCH(1,1) with GJR-GARCH variance equation and Normal innovations.
!
! Variance equation:
!   h_t = omega + alpha*y_{t-1}^2 + gamma*I(y_{t-1}<0)*y_{t-1}^2 + beta*h_{t-1}
!   h_1 = omega / (1 - alpha - gamma/2 - beta)
!   omega > 0,  alpha > 0,  gamma >= 0,  beta > 0
!   Stationarity: alpha + gamma/2 + beta < 1
!   (E[I(z<0)*z^2] = 1/2 under Normal, so effective persistence = alpha+gamma/2+beta)
!
! Unconstrained parameterisation  p(4) -> (omega, alpha, gamma, beta):
!   Let S = 1 + exp(p2) + exp(p3) + exp(p4)
!   omega = exp(p1)
!   alpha = exp(p2) / S
!   gamma = 2*exp(p3) / S     (gamma/2 enters the softmax, ensuring gamma >= 0)
!   beta  = exp(p4) / S
!   Stationarity: alpha + gamma/2 + beta = (exp(p2)+exp(p3)+exp(p4))/S < 1
! Inverse:
!   p1 = log(omega)
!   p2 = log(alpha        / slack)    where slack = 1 - alpha - gamma/2 - beta
!   p3 = log(gamma/(2*slack))
!   p4 = log(beta         / slack)

module gjr_mod
    use kind_mod,       only: dp
    use math_const_mod, only: log_sqrt_2pi, two_pi
    implicit none
    private

    real(dp), allocatable, save :: gjr_obs(:)  ! stored observations
    integer,               save :: gjr_nobs = 0

    public :: gjr_set_data, gjr_simulate, gjr_obj, &
              gjr_transform, gjr_inv_transform, &
              gjr_signed_obj, gjr_signed_transform, gjr_signed_inv_transform

contains

    subroutine gjr_set_data(y, n)
        ! Store observations for use by gjr_obj.
        integer,  intent(in) :: n    ! number of observations
        real(dp), intent(in) :: y(n) ! return series
        if (allocated(gjr_obs)) deallocate(gjr_obs)
        allocate(gjr_obs(n))
        gjr_obs  = y
        gjr_nobs = n
    end subroutine gjr_set_data

    subroutine gjr_transform(p, omega, alpha, gamma, beta)
        ! Unconstrained p(4) -> constrained (omega, alpha, gamma, beta).
        real(dp), intent(in)  :: p(4)
        real(dp), intent(out) :: omega, alpha, gamma, beta  ! GJR parameters
        real(dp) :: e2, e3, e4, s
        omega = exp(p(1))
        e2    = exp(p(2))
        e3    = exp(p(3))
        e4    = exp(p(4))
        s     = 1.0_dp + e2 + e3 + e4
        alpha = e2 / s
        gamma = 2.0_dp * e3 / s   ! gamma/2 in the softmax
        beta  = e4 / s
    end subroutine gjr_transform

    subroutine gjr_inv_transform(omega, alpha, gamma, beta, p)
        ! Constrained (omega, alpha, gamma, beta) -> unconstrained p(4).
        ! Requires omega > 0, alpha > 0, gamma >= 0, beta > 0,
        !          alpha + gamma/2 + beta < 1.
        real(dp), intent(in)  :: omega, alpha, gamma, beta  ! GJR parameters
        real(dp), intent(out) :: p(4)
        real(dp) :: slack
        slack = 1.0_dp - alpha - 0.5_dp*gamma - beta   ! stationarity slack
        p(1)  = log(omega)
        p(2)  = log(alpha / slack)
        p(3)  = log(0.5_dp*gamma / slack)
        p(4)  = log(beta  / slack)
    end subroutine gjr_inv_transform

    subroutine gjr_signed_transform(p, omega, alpha, gamma, beta)
        ! Unconstrained p(4) -> GJR parameters with signed gamma.
        ! The positive-return coefficient is alpha and the negative-return
        ! coefficient is alpha+gamma; both are constrained nonnegative.
        real(dp), intent(in)  :: p(4)
        real(dp), intent(out) :: omega, alpha, gamma, beta
        real(dp) :: eplus, eminus, ebeta, s, aplus, aminus
        omega  = exp(p(1))
        eplus  = exp(p(2))
        eminus = exp(p(3))
        ebeta  = exp(p(4))
        s      = 1.0_dp + eplus + eminus + ebeta
        aplus  = 2.0_dp * eplus / s
        aminus = 2.0_dp * eminus / s
        alpha  = aplus
        gamma  = aminus - aplus
        beta   = ebeta / s
    end subroutine gjr_signed_transform

    subroutine gjr_signed_inv_transform(omega, alpha, gamma, beta, p)
        ! Inverse for signed-gamma GJR. Requires alpha >= 0, alpha+gamma >= 0,
        ! beta > 0, and alpha + gamma/2 + beta < 1.
        real(dp), intent(in)  :: omega, alpha, gamma, beta
        real(dp), intent(out) :: p(4)
        real(dp) :: slack, aminus
        aminus = alpha + gamma
        slack = 1.0_dp - 0.5_dp*(alpha + aminus) - beta
        p(1) = log(omega)
        p(2) = log(0.5_dp*alpha / slack)
        p(3) = log(0.5_dp*aminus / slack)
        p(4) = log(beta / slack)
    end subroutine gjr_signed_inv_transform

    subroutine gjr_simulate(omega, alpha, gamma, beta, n, seed_val, y)
        ! Simulate n observations from GJR-GARCH(1,1) with Normal innovations.
        ! Uses Box-Muller; h_1 is set to the unconditional variance.
        real(dp), intent(in)  :: omega, alpha, gamma, beta  ! GJR parameters
        integer,  intent(in)  :: n                          ! number of observations
        integer,  intent(in)  :: seed_val                   ! RNG seed
        real(dp), intent(out) :: y(n)                       ! simulated return series
        real(dp) :: h, ind, u1, u2, eps
        integer  :: i, sz
        integer, allocatable :: seed_arr(:)

        call random_seed(size=sz)
        allocate(seed_arr(sz))
        seed_arr = seed_val
        call random_seed(put=seed_arr)
        deallocate(seed_arr)

        h = omega / (1.0_dp - alpha - 0.5_dp*gamma - beta)
        do i = 1, n
            do
                call random_number(u1)
                if (u1 > 0.0_dp) exit
            end do
            call random_number(u2)
            eps  = sqrt(-2.0_dp * log(u1)) * cos(two_pi * u2)
            y(i) = sqrt(h) * eps
            if (y(i) < 0.0_dp) then
                ind = 1.0_dp
            else
                ind = 0.0_dp
            end if
            h = omega + (alpha + gamma*ind)*y(i)**2 + beta*h
        end do
    end subroutine gjr_simulate

    subroutine gjr_obj(p, np, f, g)
        ! NLL and gradient w.r.t. unconstrained p for GJR-GARCH(1,1) with Normal noise.
        !
        ! f = (1/n) * [ n*log_sqrt_2pi + 0.5*sum_t(log(h_t) + y_t^2/h_t) ]
        !
        ! Variance derivative recurrences (multiplier is beta throughout):
        !   dh_t/d_omega = 1                  + beta * dh_{t-1}/d_omega
        !   dh_t/d_alpha = y_{t-1}^2          + beta * dh_{t-1}/d_alpha
        !   dh_t/d_gamma = I_{t-1}*y_{t-1}^2 + beta * dh_{t-1}/d_gamma
        !   dh_t/d_beta  = h_{t-1}            + beta * dh_{t-1}/d_beta
        !
        ! Initial values from h_1 = omega/D, D = 1 - alpha - gamma/2 - beta:
        !   dh_1/d_omega = 1/D
        !   dh_1/d_alpha = h_1/D
        !   dh_1/d_gamma = h_1/(2*D)     (factor 1/2: d(D)/d(gamma) = -1/2)
        !   dh_1/d_beta  = h_1/D
        !
        ! Chain rule to unconstrained params (g2 = gamma/2):
        !   g(1) =  grad_om * omega
        !   g(2) =  alpha * (grad_al*(1-alpha) - 2*grad_ga*g2 - grad_be*beta)
        !   g(3) =  g2    * (-grad_al*alpha + 2*grad_ga*(1-g2) - grad_be*beta)
        !   g(4) =  beta  * (-grad_al*alpha - 2*grad_ga*g2 + grad_be*(1-beta))
        integer,  intent(in)  :: np     ! number of parameters (must be 4)
        real(dp), intent(in)  :: p(np)  ! unconstrained parameters
        real(dp), intent(out) :: f      ! NLL/n
        real(dp), intent(out) :: g(np)  ! gradient of NLL/n

        real(dp) :: omega, alpha, gamma, beta
        real(dp) :: h, ind, D, h_unc, g2
        real(dp) :: dh_dom, dh_dal, dh_dga, dh_dbe
        real(dp) :: grad_om, grad_al, grad_ga, grad_be, factor
        integer  :: t

        call gjr_transform(p, omega, alpha, gamma, beta)

        g2    = 0.5_dp * gamma
        D     = 1.0_dp - alpha - g2 - beta
        h_unc = omega / D

        ! initial variance derivatives from h_1 = omega/D
        h      = h_unc
        dh_dom = 1.0_dp / D
        dh_dal = h_unc / D
        dh_dga = h_unc / (2.0_dp * D)   ! factor 1/2 from d(D)/d(gamma) = -1/2
        dh_dbe = h_unc / D

        f       = real(gjr_nobs, dp) * log_sqrt_2pi
        grad_om = 0.0_dp
        grad_al = 0.0_dp
        grad_ga = 0.0_dp
        grad_be = 0.0_dp

        do t = 1, gjr_nobs
            factor  =  1.0_dp/h - gjr_obs(t)**2 / h**2
            f       = f       + 0.5_dp * (log(h) + gjr_obs(t)**2 / h)
            grad_om = grad_om + 0.5_dp * factor * dh_dom
            grad_al = grad_al + 0.5_dp * factor * dh_dal
            grad_ga = grad_ga + 0.5_dp * factor * dh_dga
            grad_be = grad_be + 0.5_dp * factor * dh_dbe
            ! update recurrences for h_{t+1}
            if (gjr_obs(t) < 0.0_dp) then
                ind = 1.0_dp
            else
                ind = 0.0_dp
            end if
            dh_dom = 1.0_dp              + beta * dh_dom
            dh_dal = gjr_obs(t)**2       + beta * dh_dal
            dh_dga = ind*gjr_obs(t)**2   + beta * dh_dga
            dh_dbe = h                   + beta * dh_dbe
            h      = omega + (alpha + gamma*ind)*gjr_obs(t)**2 + beta*h
        end do

        ! chain rule: constrained gradients -> unconstrained gradients
        g(1) =  grad_om * omega
        g(2) =  alpha * (grad_al*(1.0_dp-alpha) - 2.0_dp*grad_ga*g2 - grad_be*beta)
        g(3) =  g2    * (-grad_al*alpha + 2.0_dp*grad_ga*(1.0_dp-g2) - grad_be*beta)
        g(4) =  beta  * (-grad_al*alpha - 2.0_dp*grad_ga*g2 + grad_be*(1.0_dp-beta))

        f = f / gjr_nobs
        g = g / gjr_nobs
    end subroutine gjr_obj

    subroutine gjr_signed_obj(p, np, f, g)
        ! NLL and gradient for GJR-GARCH with signed gamma.
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f
        real(dp), intent(out) :: g(np)

        real(dp) :: omega, alpha, gamma, beta, aplus, aminus
        real(dp) :: h, ind, D, h_unc
        real(dp) :: dh_dom, dh_dap, dh_dam, dh_dbe
        real(dp) :: grad_om, grad_ap, grad_am, grad_be, factor
        real(dp) :: eplus, eminus, ebeta, s, xplus, xminus, xbeta
        real(dp) :: dldxplus, dldxminus, dldxbeta, weighted
        integer  :: t

        call gjr_signed_transform(p, omega, alpha, gamma, beta)
        aplus  = alpha
        aminus = alpha + gamma

        D     = 1.0_dp - 0.5_dp*(aplus + aminus) - beta
        h_unc = omega / D

        h      = h_unc
        dh_dom = 1.0_dp / D
        dh_dap = h_unc / (2.0_dp * D)
        dh_dam = h_unc / (2.0_dp * D)
        dh_dbe = h_unc / D

        f       = real(gjr_nobs, dp) * log_sqrt_2pi
        grad_om = 0.0_dp
        grad_ap = 0.0_dp
        grad_am = 0.0_dp
        grad_be = 0.0_dp

        do t = 1, gjr_nobs
            factor  = 1.0_dp/h - gjr_obs(t)**2 / h**2
            f       = f + 0.5_dp * (log(h) + gjr_obs(t)**2 / h)
            grad_om = grad_om + 0.5_dp * factor * dh_dom
            grad_ap = grad_ap + 0.5_dp * factor * dh_dap
            grad_am = grad_am + 0.5_dp * factor * dh_dam
            grad_be = grad_be + 0.5_dp * factor * dh_dbe

            if (gjr_obs(t) < 0.0_dp) then
                ind = 1.0_dp
            else
                ind = 0.0_dp
            end if
            dh_dom = 1.0_dp + beta * dh_dom
            dh_dap = (1.0_dp - ind)*gjr_obs(t)**2 + beta * dh_dap
            dh_dam = ind*gjr_obs(t)**2 + beta * dh_dam
            dh_dbe = h + beta * dh_dbe
            h = omega + ((1.0_dp - ind)*aplus + ind*aminus)*gjr_obs(t)**2 + beta*h
        end do

        eplus  = exp(p(2))
        eminus = exp(p(3))
        ebeta  = exp(p(4))
        s      = 1.0_dp + eplus + eminus + ebeta
        xplus  = eplus / s
        xminus = eminus / s
        xbeta  = ebeta / s
        dldxplus  = 2.0_dp * grad_ap
        dldxminus = 2.0_dp * grad_am
        dldxbeta  = grad_be
        weighted = xplus*dldxplus + xminus*dldxminus + xbeta*dldxbeta

        g(1) = grad_om * omega
        g(2) = xplus  * (dldxplus  - weighted)
        g(3) = xminus * (dldxminus - weighted)
        g(4) = xbeta  * (dldxbeta  - weighted)

        f = f / gjr_nobs
        g = g / gjr_nobs
    end subroutine gjr_signed_obj

end module gjr_mod

! ===== end gjr.f90 =====

! ===== begin egarch.f90 =====
! EGARCH(1,1) with Normal innovations (Nelson, 1991).
!
! Variance equation (log form):
!   log(h_t) = omega + beta*log(h_{t-1}) + alpha*(|z_{t-1}| - c) + gamma*z_{t-1}
!   where z_t = y_t/sqrt(h_t),  c = sqrt(2/pi)  (E[|z|] under Normal)
!   log(h_1) = omega / (1 - beta)
!   omega, alpha, gamma unconstrained;  |beta| < 1
!
! Unconstrained parameterisation  p(4) -> (omega, alpha, gamma, beta):
!   omega = p(1)                        (free)
!   alpha = p(2)                        (free)
!   gamma = p(3)                        (free)
!   beta  = tanh(p(4))                  (enforces |beta| < 1)
! Inverse:
!   p(1) = omega
!   p(2) = alpha
!   p(3) = gamma
!   p(4) = arctanh(beta) = 0.5*log((1+beta)/(1-beta))

module egarch_mod
    use kind_mod,       only: dp
    use math_const_mod, only: log_sqrt_2pi, two_pi, pi
    implicit none
    private

    real(dp), allocatable, save :: ega_obs(:)  ! stored observations
    integer,               save :: ega_nobs = 0

    public :: egarch_set_data, egarch_simulate, egarch_obj, &
              egarch_transform, egarch_inv_transform

contains

    subroutine egarch_set_data(y, n)
        ! Store observations for use by egarch_obj.
        integer,  intent(in) :: n    ! number of observations
        real(dp), intent(in) :: y(n) ! return series
        if (allocated(ega_obs)) deallocate(ega_obs)
        allocate(ega_obs(n))
        ega_obs  = y
        ega_nobs = n
    end subroutine egarch_set_data

    subroutine egarch_transform(p, omega, alpha, gamma, beta)
        ! Unconstrained p(4) -> constrained (omega, alpha, gamma, beta).
        real(dp), intent(in)  :: p(4)
        real(dp), intent(out) :: omega, alpha, gamma, beta
        omega = p(1)
        alpha = p(2)
        gamma = p(3)
        beta  = tanh(p(4))
    end subroutine egarch_transform

    subroutine egarch_inv_transform(omega, alpha, gamma, beta, p)
        ! Constrained (omega, alpha, gamma, beta) -> unconstrained p(4).
        ! Requires |beta| < 1.
        real(dp), intent(in)  :: omega, alpha, gamma, beta
        real(dp), intent(out) :: p(4)
        p(1) = omega
        p(2) = alpha
        p(3) = gamma
        p(4) = 0.5_dp * log((1.0_dp + beta) / (1.0_dp - beta))  ! arctanh
    end subroutine egarch_inv_transform

    subroutine egarch_simulate(omega, alpha, gamma, beta, n, seed_val, y)
        ! Simulate n observations from EGARCH(1,1) with Normal innovations.
        ! Uses Box-Muller; log(h_1) is set to the unconditional log-variance.
        real(dp), intent(in)  :: omega, alpha, gamma, beta
        integer,  intent(in)  :: n, seed_val
        real(dp), intent(out) :: y(n)
        real(dp) :: lh, h, z, u1, u2, eps, c_eg
        integer  :: i, sz
        integer, allocatable :: seed_arr(:)

        call random_seed(size=sz)
        allocate(seed_arr(sz))
        seed_arr = seed_val
        call random_seed(put=seed_arr)
        deallocate(seed_arr)

        c_eg = sqrt(2.0_dp / pi)
        lh   = omega / (1.0_dp - beta)   ! unconditional log-variance
        do i = 1, n
            h = exp(lh)
            do
                call random_number(u1)
                if (u1 > 0.0_dp) exit
            end do
            call random_number(u2)
            eps  = sqrt(-2.0_dp * log(u1)) * cos(two_pi * u2)
            y(i) = sqrt(h) * eps
            z    = eps                    ! z_t = y_t/sqrt(h_t) = eps
            lh   = omega + beta*lh + alpha*(abs(z) - c_eg) + gamma*z
        end do
    end subroutine egarch_simulate

    subroutine egarch_obj(p, np, f, g)
        ! NLL and gradient w.r.t. unconstrained p for EGARCH(1,1) with Normal noise.
        !
        ! f = (1/n) * [ n*log_sqrt_2pi + 0.5*sum_t(lh_t + y_t^2*exp(-lh_t)) ]
        !
        ! Define z_t = y_t/sqrt(h_t),  c = sqrt(2/pi)
        !        kappa_t = beta - 0.5*(alpha*|z_{t-1}| + gamma*z_{t-1})
        !          (from d(lh_t)/d(lh_{t-1}) via dz_{t-1}/d(lh_{t-1}) = -z_{t-1}/2)
        !
        ! Log-variance derivative recurrences:
        !   dlh_t/d_omega = 1                    + kappa_t * dlh_{t-1}/d_omega
        !   dlh_t/d_alpha = (|z_{t-1}| - c)      + kappa_t * dlh_{t-1}/d_alpha
        !   dlh_t/d_gamma = z_{t-1}              + kappa_t * dlh_{t-1}/d_gamma
        !   dlh_t/d_beta  = lh_{t-1}             + kappa_t * dlh_{t-1}/d_beta
        !
        ! Initial values from lh_1 = omega/(1-beta):
        !   dlh_1/d_omega = 1/(1-beta)
        !   dlh_1/d_alpha = 0
        !   dlh_1/d_gamma = 0
        !   dlh_1/d_beta  = omega/(1-beta)^2 = lh_1/(1-beta)
        !
        ! Chain rule to unconstrained params:
        !   g(1) = grad_om                       (omega = p1, free)
        !   g(2) = grad_al                       (alpha = p2, free)
        !   g(3) = grad_ga                       (gamma = p3, free)
        !   g(4) = grad_be * (1 - beta^2)        (beta = tanh(p4))
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f
        real(dp), intent(out) :: g(np)

        real(dp) :: omega, alpha, gamma, beta
        real(dp) :: lh, h, z, abs_z, kappa, c_eg
        real(dp) :: dlh_dom, dlh_dal, dlh_dga, dlh_dbe
        real(dp) :: grad_om, grad_al, grad_ga, grad_be, factor
        integer  :: t

        call egarch_transform(p, omega, alpha, gamma, beta)

        c_eg = sqrt(2.0_dp / pi)
        lh   = omega / (1.0_dp - beta)   ! unconditional log-variance

        ! initial log-variance derivatives from lh_1 = omega/(1-beta)
        dlh_dom = 1.0_dp / (1.0_dp - beta)
        dlh_dal = 0.0_dp
        dlh_dga = 0.0_dp
        dlh_dbe = lh / (1.0_dp - beta)   ! = omega/(1-beta)^2

        f       = real(ega_nobs, dp) * log_sqrt_2pi
        grad_om = 0.0_dp
        grad_al = 0.0_dp
        grad_ga = 0.0_dp
        grad_be = 0.0_dp

        do t = 1, ega_nobs
            h       = exp(lh)
            z       = ega_obs(t) / sqrt(h)
            factor  = 0.5_dp * (1.0_dp - z**2)
            f       = f       + 0.5_dp * (lh + ega_obs(t)**2 / h)
            grad_om = grad_om + factor * dlh_dom
            grad_al = grad_al + factor * dlh_dal
            grad_ga = grad_ga + factor * dlh_dga
            grad_be = grad_be + factor * dlh_dbe
            ! update recurrences for lh_{t+1}
            abs_z   = abs(z)
            kappa   = beta - 0.5_dp * (alpha * abs_z + gamma * z)
            dlh_dom = 1.0_dp             + kappa * dlh_dom
            dlh_dal = (abs_z - c_eg)     + kappa * dlh_dal
            dlh_dga = z                  + kappa * dlh_dga
            dlh_dbe = lh                 + kappa * dlh_dbe
            lh      = omega + beta*lh + alpha*(abs_z - c_eg) + gamma*z
        end do

        ! chain rule: constrained gradients -> unconstrained gradients
        g(1) = grad_om
        g(2) = grad_al
        g(3) = grad_ga
        g(4) = grad_be * (1.0_dp - beta**2)

        f = f / ega_nobs
        g = g / ega_nobs
    end subroutine egarch_obj

end module egarch_mod

! ===== end egarch.f90 =====

! ===== begin fgarch.f90 =====
module fgarch_mod
    ! Family GARCH: Hentschel (J. Fin. Econ. 39, 1995, pp. 71-104)
    !
    ! Variance recursion (normalised shock z_t = y_t / σ_t):
    !   h_t  = (σ_t^λ − 1) / λ                         [Box-Cox of σ]
    !   h_t  = ω + α · σ_{t-1}^ν · f(z_{t-1})^ν + β · h_{t-1}
    !   f(z) = |z − b| − c·(z − b)
    !
    ! Parameters p (7 for Normal, 8 for t):
    !   p(1) = ω  (free)
    !   p(2) = log α       → α = exp(p2) > 0
    !   p(3) = logit β     → β = sigmoid(p3) ∈ (0,1)
    !   p(4) = log λ       → λ = exp(p4) > 0
    !   p(5) = log ν       → ν = exp(p5) > 0
    !   p(6) = b           (free shift)
    !   p(7) = atanh c     → c = tanh(p7) ∈ (−1,1)
    !   p(8) = p_ν_t       → ν_t = 2 + 98/(1+exp(−p8))  [t only]
    !
    ! Gradient: analytical for p(1..7); 2-point FD for p(8) (t-df).

    use kind_mod, only: dp
    use math_const_mod, only: log_sqrt_2pi, sqrt2
    implicit none
    private

    public :: fg_dist_normal, fg_dist_t
    public :: fgarch_set_data, fgarch_set_dist, fgarch_np
    public :: fgarch_obj, fgarch_transform, fgarch_inv_transform
    public :: fgarch_vol_ann, fgarch_skew_kurt
    public :: fgarch22_obj, fgarch22_transform, fgarch22_inv_transform
    public :: fgarch22_vol_ann, fgarch22_skew_kurt
    public :: fgarch_twist_obj, fgarch_twist_transform, fgarch_twist_inv_transform
    public :: fgarch_twist_vol_ann, fgarch_twist_skew_kurt

    integer, parameter :: fg_dist_normal = 1
    integer, parameter :: fg_dist_t      = 2
    integer, parameter :: fg_np_base     = 7
    integer, parameter :: fg22_np        = 5
    integer, parameter :: fg_twist_np    = 5

    integer               :: fg_nobs = 0
    integer               :: fg_dist = fg_dist_normal
    real(dp), allocatable :: fg_y(:)

contains

    subroutine fgarch_set_data(y, n)
        real(dp), intent(in) :: y(n)
        integer,  intent(in) :: n
        fg_nobs = n
        if (allocated(fg_y)) deallocate(fg_y)
        allocate(fg_y(n))
        fg_y = y
    end subroutine

    subroutine fgarch_set_dist(idist)
        integer, intent(in) :: idist
        fg_dist = idist
    end subroutine

    integer function fgarch_np()
        if (fg_dist == fg_dist_t) then
            fgarch_np = fg_np_base + 1
        else
            fgarch_np = fg_np_base
        end if
    end function

    subroutine fgarch_transform(p, np, omega, alpha, beta, lam, nu, b, c, nu_t)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: omega, alpha, beta, lam, nu, b, c, nu_t
        omega = p(1)
        alpha = exp(p(2))
        beta  = 1.0_dp / (1.0_dp + exp(-p(3)))
        lam   = exp(p(4))
        nu    = exp(p(5))
        b     = p(6)
        c     = tanh(p(7))
        nu_t  = 0.0_dp
        if (np > fg_np_base) nu_t = 2.0_dp + 98.0_dp / (1.0_dp + exp(-p(8)))
    end subroutine

    subroutine fgarch_inv_transform(omega, alpha, beta, lam, nu, b, c, nu_t, p)
        real(dp), intent(in)  :: omega, alpha, beta, lam, nu, b, c, nu_t
        real(dp), intent(out) :: p(:)
        p(1) = omega
        p(2) = log(alpha)
        p(3) = log(beta / (1.0_dp - beta))
        p(4) = log(lam)
        p(5) = log(nu)
        p(6) = b
        p(7) = 0.5_dp * log((1.0_dp + c) / (1.0_dp - c))
        if (size(p) >= fg_np_base + 1) &
            p(8) = -log(98.0_dp / (nu_t - 2.0_dp) - 1.0_dp)
    end subroutine

    subroutine fgarch22_transform(p, omega, alpha, beta, b, c)
        real(dp), intent(in)  :: p(fg22_np)
        real(dp), intent(out) :: omega, alpha, beta, b, c
        omega = p(1)
        alpha = exp(p(2))
        beta  = 1.0_dp / (1.0_dp + exp(-p(3)))
        b     = p(4)
        c     = tanh(p(5))
    end subroutine

    subroutine fgarch22_inv_transform(omega, alpha, beta, b, c, p)
        real(dp), intent(in)  :: omega, alpha, beta, b, c
        real(dp), intent(out) :: p(fg22_np)
        p(1) = omega
        p(2) = log(alpha)
        p(3) = log(beta / (1.0_dp - beta))
        p(4) = b
        p(5) = 0.5_dp * log((1.0_dp + c) / (1.0_dp - c))
    end subroutine

    subroutine fgarch_twist_transform(p, omega, alpha, beta, theta, c)
        real(dp), intent(in)  :: p(fg_twist_np)
        real(dp), intent(out) :: omega, alpha, beta, theta, c
        real(dp) :: e2, e3, s, aa, moment, dmth, dmc
        omega = exp(p(1))
        theta = p(4)
        c = tanh(p(5))
        call fgarch_twist_moment(theta, c, moment, dmth, dmc)
        e2 = exp(p(2));  e3 = exp(p(3))
        s  = 1.0_dp + e2 + e3
        aa = e2 / s
        beta = e3 / s
        alpha = aa / moment
    end subroutine

    subroutine fgarch_twist_inv_transform(omega, alpha, beta, theta, c, p)
        real(dp), intent(in)  :: omega, alpha, beta, theta, c
        real(dp), intent(out) :: p(fg_twist_np)
        real(dp) :: aa, gam, moment, dmth, dmc
        call fgarch_twist_moment(theta, c, moment, dmth, dmc)
        aa = alpha * moment
        gam = 1.0_dp - aa - beta
        p(1) = log(omega)
        p(2) = log(aa / gam)
        p(3) = log(beta / gam)
        p(4) = theta
        p(5) = 0.5_dp * log((1.0_dp + c) / (1.0_dp - c))
    end subroutine

    ! NLL per obs only (used for FD on p(8)).
    subroutine fgarch_nll(p, np, f)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f
        real(dp) :: omega, alpha, beta, lam, nu, b, c, nu_t
        real(dp) :: sigma, h, z, fz, fnu, h_new, slam, sigma_new, nll, lscore, s0
        real(dp), parameter :: log_sqrt_2pi = 0.9189385332046727_dp
        real(dp), parameter :: pi = 3.14159265358979323846_dp
        integer  :: t
        call fgarch_transform(p, np, omega, alpha, beta, lam, nu, b, c, nu_t)
        s0    = sqrt(sum(fg_y**2) / fg_nobs)
        sigma = s0
        h     = (sigma**lam - 1.0_dp) / lam
        nll   = 0.0_dp
        do t = 1, fg_nobs
            z = fg_y(t) / sigma
            if (fg_dist == fg_dist_t) then
                lscore = log(sigma) &
                       + log_gamma(0.5_dp*nu_t) - log_gamma(0.5_dp*(nu_t+1.0_dp)) &
                       + 0.5_dp*log(pi*(nu_t-2.0_dp)) &
                       + 0.5_dp*(nu_t+1.0_dp)*log(1.0_dp + z**2/(nu_t-2.0_dp))
            else
                lscore = log_sqrt_2pi + log(sigma) + 0.5_dp*z**2
            end if
            nll  = nll + lscore
            fz   = abs(z - b) - c*(z - b)
            if (fz < 0.0_dp) fz = 0.0_dp
            fnu  = fz**nu
            h_new = omega + alpha * sigma**nu * fnu + beta * h
            slam  = 1.0_dp + lam * h_new
            if (slam <= 0.0_dp) then
                f = 1.0e30_dp
                return
            end if
            sigma_new = slam**(1.0_dp / lam)
            h     = h_new
            sigma = sigma_new
        end do
        f = nll / fg_nobs
    end subroutine

    ! NLL + analytical gradient for p(1..7).
    ! Tracks s = dh/dp and q = dσ/dp through the filter.
    subroutine fgarch_nll_grad(p, np, f, g7)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f, g7(fg_np_base)
        real(dp) :: omega, alpha, beta, lam, nu, b, c, nu_t
        real(dp) :: sigma, h, z, fz, fnu, kappa, h_new, slam, sigma_new, nll, lscore, s0
        real(dp) :: Dfz, sign_zb, A_sigma, dsh, lam_dir, xi
        real(dp) :: direct(fg_np_base)
        real(dp) :: sv(fg_np_base), qv(fg_np_base), sv_n(fg_np_base), qv_n(fg_np_base)
        real(dp), parameter :: log_sqrt_2pi = 0.9189385332046727_dp
        real(dp), parameter :: pi  = 3.14159265358979323846_dp
        real(dp), parameter :: eps = 1.0e-12_dp
        integer  :: t, k

        call fgarch_transform(p, np, omega, alpha, beta, lam, nu, b, c, nu_t)

        s0    = sqrt(sum(fg_y**2) / fg_nobs)
        sigma = s0
        h     = (sigma**lam - 1.0_dp) / lam

        ! sigma_0 = s0 is fixed → qv_0 = 0.
        ! h_0 = (s0^lam - 1)/lam depends on lam: dh_0/d(log_lam) = s0^lam*log(s0) - h_0.
        qv = 0.0_dp
        sv = 0.0_dp
        sv(4) = sigma**lam * log(sigma) - h    ! init sensitivity for log_lam

        nll = 0.0_dp
        g7  = 0.0_dp

        do t = 1, fg_nobs
            z = fg_y(t) / sigma

            ! NLL contribution and dl/dsigma (= xi).
            if (fg_dist == fg_dist_t) then
                lscore = log(sigma) &
                       + log_gamma(0.5_dp*nu_t) - log_gamma(0.5_dp*(nu_t+1.0_dp)) &
                       + 0.5_dp*log(pi*(nu_t-2.0_dp)) &
                       + 0.5_dp*(nu_t+1.0_dp)*log(1.0_dp + z**2/(nu_t-2.0_dp))
                xi = 1.0_dp/sigma &
                     - (nu_t+1.0_dp)*z**2 / (sigma*(nu_t-2.0_dp + z**2))
            else
                lscore = log_sqrt_2pi + log(sigma) + 0.5_dp*z**2
                xi = (1.0_dp - z**2) / sigma
            end if
            nll = nll + lscore

            ! Gradient from this observation: dl_t/dp = xi * qv.
            do k = 1, fg_np_base
                g7(k) = g7(k) + xi * qv(k)
            end do

            ! News impact at time t.
            if (z >= b) then
                sign_zb = 1.0_dp
            else
                sign_zb = -1.0_dp
            end if
            fz = abs(z - b) - c*(z - b)
            if (fz < 0.0_dp) fz = 0.0_dp

            if (fz > eps) then
                Dfz = nu * fz**(nu - 1.0_dp) * (sign_zb - c)
            else
                Dfz = 0.0_dp
            end if
            fnu   = fz**nu
            kappa = alpha * sigma**nu * fnu

            ! dh_{t+1}/dσ_t (holding h_t fixed).
            A_sigma = alpha * sigma**(nu - 1.0_dp) * (nu*fnu - Dfz*z)

            ! Direct contributions to dh_{t+1}/dp (holding σ_t, h_t fixed).
            direct(1) = 1.0_dp                                ! ω
            direct(2) = kappa                                 ! log α
            direct(3) = h * beta * (1.0_dp - beta)            ! logit β
            direct(4) = 0.0_dp                                ! log λ (none)
            if (fz > eps) then
                direct(5) = nu * kappa * (log(sigma) + log(fz))  ! log ν
                direct(6) = -alpha * sigma**nu * Dfz              ! b
                direct(7) = -alpha * sigma**nu &                  ! atanh c
                            * nu * fz**(nu-1.0_dp) * (z-b) * (1.0_dp - c**2)
            else
                direct(5) = 0.0_dp
                direct(6) = 0.0_dp
                direct(7) = 0.0_dp
            end if

            ! Update h and σ.
            h_new = omega + kappa + beta * h
            slam  = 1.0_dp + lam * h_new
            if (slam <= 0.0_dp) then
                f  = 1.0e30_dp
                g7 = 0.0_dp
                return
            end if
            sigma_new = slam**(1.0_dp / lam)
            dsh     = sigma_new**(1.0_dp - lam)          ! dsigma/dh = sigma^(1-lam)
            lam_dir = sigma_new * (lam * h_new / sigma_new**lam - log(sigma_new))

            ! Propagate sensitivities.
            do k = 1, fg_np_base
                sv_n(k) = direct(k) + beta * sv(k) + A_sigma * qv(k)
                qv_n(k) = dsh * sv_n(k)
            end do
            qv_n(4) = qv_n(4) + lam_dir    ! extra Box-Cox contribution to λ

            sv    = sv_n
            qv    = qv_n
            h     = h_new
            sigma = sigma_new
        end do

        f  = nll  / fg_nobs
        g7 = g7   / fg_nobs
    end subroutine

    ! Objective for BFGS: analytical gradient for p(1..7), 2-pt FD for p(8).
    subroutine fgarch_obj(p, np, f, g)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f, g(np)
        real(dp) :: pp(np), fm, fp
        real(dp), parameter :: h_fd = 1.0e-5_dp

        call fgarch_nll_grad(p, np, f, g(1:fg_np_base))

        if (np > fg_np_base) then
            pp = p;  pp(fg_np_base+1) = p(fg_np_base+1) + h_fd
            call fgarch_nll(pp, np, fp)
            pp = p;  pp(fg_np_base+1) = p(fg_np_base+1) - h_fd
            call fgarch_nll(pp, np, fm)
            g(fg_np_base+1) = (fp - fm) / (2.0_dp * h_fd)
        end if
    end subroutine

    subroutine fgarch22_obj(p, np, f, g)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f, g(np)
        real(dp) :: omega, alpha, beta, b, c
        real(dp) :: sigma, h, z, fz, fz2, kappa, h_new, slam, sigma_new, nll, lscore, s0
        real(dp) :: dfz2_dz, sign_zb, A_sigma, dsh, xi
        real(dp) :: direct(fg22_np), sv(fg22_np), qv(fg22_np), sv_n(fg22_np), qv_n(fg22_np)
        real(dp), parameter :: log_sqrt_2pi = 0.9189385332046727_dp
        real(dp), parameter :: eps = 1.0e-12_dp
        integer :: t, k

        call fgarch22_transform(p(1:fg22_np), omega, alpha, beta, b, c)

        s0    = sqrt(sum(fg_y**2) / fg_nobs)
        sigma = s0
        h     = 0.5_dp * (sigma**2 - 1.0_dp)
        qv    = 0.0_dp
        sv    = 0.0_dp
        nll   = 0.0_dp
        g     = 0.0_dp

        do t = 1, fg_nobs
            z      = fg_y(t) / sigma
            lscore = log_sqrt_2pi + log(sigma) + 0.5_dp*z**2
            xi     = (1.0_dp - z**2) / sigma
            nll    = nll + lscore

            do k = 1, fg22_np
                g(k) = g(k) + xi * qv(k)
            end do

            if (z >= b) then
                sign_zb = 1.0_dp
            else
                sign_zb = -1.0_dp
            end if
            fz = abs(z - b) - c*(z - b)
            if (fz < 0.0_dp) fz = 0.0_dp

            if (fz > eps) then
                dfz2_dz = 2.0_dp * fz * (sign_zb - c)
            else
                dfz2_dz = 0.0_dp
            end if
            fz2   = fz**2
            kappa = alpha * sigma**2 * fz2

            A_sigma = alpha * sigma * (2.0_dp*fz2 - dfz2_dz*z)

            direct(1) = 1.0_dp
            direct(2) = kappa
            direct(3) = h * beta * (1.0_dp - beta)
            direct(4) = -alpha * sigma**2 * dfz2_dz
            if (fz > eps) then
                direct(5) = -alpha * sigma**2 * 2.0_dp * fz * (z - b) * (1.0_dp - c**2)
            else
                direct(5) = 0.0_dp
            end if

            h_new = omega + kappa + beta * h
            slam  = 1.0_dp + 2.0_dp * h_new
            if (slam <= 0.0_dp) then
                f = 1.0e30_dp
                g = 0.0_dp
                return
            end if
            sigma_new = sqrt(slam)
            dsh = 1.0_dp / sigma_new

            do k = 1, fg22_np
                sv_n(k) = direct(k) + beta * sv(k) + A_sigma * qv(k)
                qv_n(k) = dsh * sv_n(k)
            end do

            sv    = sv_n
            qv    = qv_n
            h     = h_new
            sigma = sigma_new
        end do

        f = nll / fg_nobs
        g = g / fg_nobs
    end subroutine

    ! Run filter; return annualised vol% = sqrt(252 * mean(σ_t^2)) * 100.
    subroutine fgarch_twist_obj(p, np, f, g)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: f, g(np)
        real(dp) :: omega, alpha, beta, theta, c
        real(dp) :: h, sqrth, z, x, q, q2, sign_x, kappa, nll, factor
        real(dp) :: moment, dmth, dmc, aa, D
        real(dp) :: dh_dom, dh_dal, dh_dbe, dh_dth, dh_dc
        real(dp) :: grad_om, grad_al, grad_be, grad_th, grad_c
        real(dp), parameter :: eps = 1.0e-12_dp
        integer :: t

        call fgarch_twist_transform(p(1:fg_twist_np), omega, alpha, beta, theta, c)
        call fgarch_twist_moment(theta, c, moment, dmth, dmc)

        D = 1.0_dp - alpha*moment - beta
        h = omega / D
        dh_dom = 1.0_dp / D
        dh_dal = h * moment / D
        dh_dbe = h / D
        dh_dth = h * alpha * dmth / D
        dh_dc  = h * alpha * dmc  / D

        nll = 0.0_dp
        grad_om = 0.0_dp;  grad_al = 0.0_dp;  grad_be = 0.0_dp
        grad_th = 0.0_dp;  grad_c  = 0.0_dp

        do t = 1, fg_nobs
            h = max(h, eps)
            sqrth = sqrt(h)
            z = fg_y(t) / sqrth
            nll = nll + log_sqrt_2pi + 0.5_dp * (log(h) + fg_y(t)**2 / h)
            factor = 1.0_dp/h - fg_y(t)**2 / h**2
            grad_om = grad_om + 0.5_dp * factor * dh_dom
            grad_al = grad_al + 0.5_dp * factor * dh_dal
            grad_be = grad_be + 0.5_dp * factor * dh_dbe
            grad_th = grad_th + 0.5_dp * factor * dh_dth
            grad_c  = grad_c  + 0.5_dp * factor * dh_dc

            x = z - theta
            sign_x = merge(1.0_dp, -1.0_dp, x >= 0.0_dp)
            q = abs(x) - c*x
            q2 = q*q
            kappa = beta + alpha * (q2 - q * (sign_x - c) * z)

            dh_dom = 1.0_dp                                  + kappa * dh_dom
            dh_dal = h * q2                                  + kappa * dh_dal
            dh_dbe = h                                       + kappa * dh_dbe
            dh_dth = -2.0_dp * alpha * h * q * (sign_x - c)  + kappa * dh_dth
            dh_dc  = -2.0_dp * alpha * h * q * x             + kappa * dh_dc
            h = omega + alpha * h * q2 + beta * h
        end do

        aa = alpha * moment
        g(1) = grad_om * omega
        g(2) = grad_al * alpha*(1.0_dp - aa) - grad_be * aa*beta
        g(3) = -grad_al * alpha*beta + grad_be * beta*(1.0_dp - beta)
        g(4) = grad_al * (-alpha*dmth/moment) + grad_th
        g(5) = (grad_al * (-alpha*dmc/moment) + grad_c) * (1.0_dp - c**2)

        f = nll / fg_nobs
        g = g / fg_nobs
    end subroutine

    subroutine fgarch_twist_moment(theta, c, moment, dmth, dmc)
        real(dp), intent(in)  :: theta, c
        real(dp), intent(out) :: moment, dmth, dmc
        real(dp) :: Phi, ph, A, B, epos, eneg, one_minus_c, one_plus_c

        Phi = 0.5_dp * (1.0_dp + erf(theta / sqrt2))
        ph  = exp(-0.5_dp * theta**2 - log_sqrt_2pi)

        A = (1.0_dp + theta**2) * (1.0_dp - Phi) - theta * ph
        B = (1.0_dp + theta**2) * Phi + theta * ph
        epos = ph - theta * (1.0_dp - Phi)
        eneg = -ph - theta * Phi

        one_minus_c = 1.0_dp - c
        one_plus_c  = 1.0_dp + c
        moment = one_minus_c**2 * A + one_plus_c**2 * B
        dmth   = -2.0_dp * (one_minus_c**2 * epos + one_plus_c**2 * eneg)
        dmc    = -2.0_dp * one_minus_c * A + 2.0_dp * one_plus_c * B
    end subroutine

    subroutine fgarch_vol_ann(p, np, vol_ann_pct)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: vol_ann_pct
        real(dp), parameter   :: trading_days = 252.0_dp
        real(dp) :: omega, alpha, beta, lam, nu, b, c, nu_t
        real(dp) :: sigma, h, z, fz, fnu, h_new, slam, sigma_new, s0, sum_var
        integer  :: t
        call fgarch_transform(p, np, omega, alpha, beta, lam, nu, b, c, nu_t)
        s0      = sqrt(sum(fg_y**2) / fg_nobs)
        sigma   = s0
        h       = (sigma**lam - 1.0_dp) / lam
        sum_var = 0.0_dp
        do t = 1, fg_nobs
            sum_var = sum_var + sigma**2
            z     = fg_y(t) / sigma
            fz    = abs(z - b) - c*(z - b)
            if (fz < 0.0_dp) fz = 0.0_dp
            fnu   = fz**nu
            h_new = omega + alpha * sigma**nu * fnu + beta * h
            slam  = 1.0_dp + lam * h_new
            if (slam <= 0.0_dp) slam = 1.0e-30_dp
            sigma_new = slam**(1.0_dp / lam)
            h     = h_new
            sigma = sigma_new
        end do
        vol_ann_pct = sqrt(trading_days * sum_var / fg_nobs) * 100.0_dp
    end subroutine

    subroutine fgarch22_vol_ann(p, np, vol_ann_pct)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: vol_ann_pct
        real(dp), parameter :: trading_days = 252.0_dp
        real(dp) :: omega, alpha, beta, b, c
        real(dp) :: sigma, h, z, fz, h_new, slam, sigma_new, s0, sum_var
        integer :: t
        call fgarch22_transform(p(1:fg22_np), omega, alpha, beta, b, c)
        s0      = sqrt(sum(fg_y**2) / fg_nobs)
        sigma   = s0
        h       = 0.5_dp * (sigma**2 - 1.0_dp)
        sum_var = 0.0_dp
        do t = 1, fg_nobs
            sum_var = sum_var + sigma**2
            z       = fg_y(t) / sigma
            fz      = abs(z - b) - c*(z - b)
            if (fz < 0.0_dp) fz = 0.0_dp
            h_new = omega + alpha * sigma**2 * fz**2 + beta * h
            slam  = 1.0_dp + 2.0_dp * h_new
            if (slam <= 0.0_dp) slam = 1.0e-30_dp
            sigma_new = sqrt(slam)
            h     = h_new
            sigma = sigma_new
        end do
        vol_ann_pct = sqrt(trading_days * sum_var / fg_nobs) * 100.0_dp
    end subroutine

    subroutine fgarch_twist_vol_ann(p, np, vol_ann_pct)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: vol_ann_pct
        real(dp), parameter :: trading_days = 252.0_dp
        real(dp) :: omega, alpha, beta, theta, c
        real(dp) :: h, z, x, q, moment, dmth, dmc, sum_var
        integer :: t

        call fgarch_twist_transform(p(1:fg_twist_np), omega, alpha, beta, theta, c)
        call fgarch_twist_moment(theta, c, moment, dmth, dmc)
        h = omega / (1.0_dp - alpha*moment - beta)
        sum_var = 0.0_dp
        do t = 1, fg_nobs
            h = max(h, 1.0e-12_dp)
            sum_var = sum_var + h
            z = fg_y(t) / sqrt(h)
            x = z - theta
            q = abs(x) - c*x
            h = omega + alpha * h * q**2 + beta * h
        end do
        vol_ann_pct = sqrt(trading_days * sum_var / fg_nobs) * 100.0_dp
    end subroutine

    ! Run filter; return skew and excess kurtosis of z_t = y_t / σ_t.
    subroutine fgarch_skew_kurt(p, np, skew, kurt)
        integer,  intent(in)  :: np
        real(dp), intent(in)  :: p(np)
        real(dp), intent(out) :: skew, kurt
        real(dp) :: omega, alpha, beta, lam, nu, b, c, nu_t
        real(dp) :: sigma, h, z, fz, fnu, h_new, slam, sigma_new, s0
        real(dp) :: zm, zv, zs, zk, dz, rn
        real(dp), allocatable :: zz(:)
        integer  :: t
        call fgarch_transform(p, np, omega, alpha, beta, lam, nu, b, c, nu_t)
        s0    = sqrt(sum(fg_y**2) / fg_nobs)
        sigma = s0
        h     = (sigma**lam - 1.0_dp) / lam
        allocate(zz(fg_nobs))
        do t = 1, fg_nobs
            z     = fg_y(t) / sigma
            zz(t) = z
            fz    = abs(z - b) - c*(z - b)
            if (fz < 0.0_dp) fz = 0.0_dp
            fnu   = fz**nu
            h_new = omega + alpha * sigma**nu * fnu + beta * h
            slam  = 1.0_dp + lam * h_new
            if (slam <= 0.0_dp) slam = 1.0e-30_dp
            sigma_new = slam**(1.0_dp / lam)
            h     = h_new
            sigma = sigma_new
        end do
        rn = real(fg_nobs, dp)
        zm = sum(zz) / rn
        zv = 0.0_dp;  zs = 0.0_dp;  zk = 0.0_dp
        do t = 1, fg_nobs
            dz = zz(t) - zm
            zv = zv + dz**2
            zs = zs + dz**3
            zk = zk + dz**4
        end do
        zv   = zv / rn
        skew = (zs / rn) / zv**1.5_dp
        kurt = (zk / rn) / zv**2 - 3.0_dp
        deallocate(zz)
    end subroutine

