        do t = 1, n
            f_best = f_best + 0.5_dp * (log(max(variance(t), 1.0e-12_dp)) + y(t)**2 / max(variance(t), 1.0e-12_dp))
        end do
        f_best = f_best / real(n, dp)
        params = garch_params_t()
        niter_best = 0
        converged_best = .true.
        deallocate(variance)
    end subroutine fit_riskmetrics2006

